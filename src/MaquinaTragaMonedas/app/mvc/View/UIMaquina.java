    public int conversionMonedas(String numero) {
        int numeroMonedas = 0;
        try {
            numeroMonedas = Math.abs(Integer.parseInt(numero));
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "No se logro convertir correctamente: " + numero + " a número", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return numeroMonedas;
    }

