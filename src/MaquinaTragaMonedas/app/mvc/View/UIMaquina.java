    private void jBtnApuestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnApuestaActionPerformed
        if (!(jTxtApuesta.getText().isEmpty()) && datosMaquina.getHandel()) {
            monedasDisponibles = Integer.parseInt(jLabelCreditos.getText());
            valorApuesta = conversionMonedas(jTxtApuesta.getText());
            if (monedasDisponibles >= valorApuesta) {
                // Inicializamos el Timer para la animación de los símbolos
                this.jBtnApuesta.setEnabled(false);
                monedasDisponibles = monedasDisponibles - valorApuesta;
                jLabelCreditos.setText(String.valueOf(monedasDisponibles));
                datosMaquina.setValorApuesta(valorApuesta);
                jTxtApuesta.setText(null);                
                Timer timer = new Timer(100, new ActionListener() {
                    int contador = 0;
                    Random random = new Random();

                    @Override
                    public void actionPerformed(ActionEvent e) {
                        // Actualizamos los JLabels nombrado con símbolos aleatorios durante la animación
                        labelCarrete1.setText(datosMaquina.getSimbolos()[random.nextInt(datosMaquina.getSimbolos().length)]);
                        labelCarrete2.setText(datosMaquina.getSimbolos()[random.nextInt(datosMaquina.getSimbolos().length)]);
                        labelCarrete3.setText(datosMaquina.getSimbolos()[random.nextInt(datosMaquina.getSimbolos().length)]);
                        
                        contador++;

                        if (contador >= 20) { // Detenemos la animación después de 20 ciclos
                            ((Timer) e.getSource()).stop();

                            // Usamos el método asíncrono para obtener el resultado final
                            Future<String[]> futureResultado = controlador.girarCarretesSimultaneamente();

                            try {
                                // Mostramos el resultado final en los JLabels
                                String[] resultadoFinal = futureResultado.get(); // Bloquea hasta que obtenga el resultado
                                labelCarrete1.setText(resultadoFinal[0]);
                                labelCarrete2.setText(resultadoFinal[1]);
                                labelCarrete3.setText(resultadoFinal[2]);

                                // Calculamos las ganancias aquí después de obtener el resultado
                                controlador.calcularGanancias(resultadoFinal);

                                // Verificamos si hay ganancias
                                if (datosMaquina.getGananciasApuesta() > 0) {
                                    gananciaApuesta = datosMaquina.getGananciasApuesta();
                                    JOptionPane.showMessageDialog(null, "Felicidades, has ganado " + gananciaApuesta + " monedas", "Juego Ganado", JOptionPane.INFORMATION_MESSAGE);
                                    monedasDisponibles = monedasDisponibles + gananciaApuesta;
                                    jLabelCreditos.setText(String.valueOf(monedasDisponibles));                                    
                                }
                            } catch (InterruptedException | ExecutionException ex) {
                                JOptionPane.showMessageDialog(null, "Ocurrió un error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                            }
                        }
                    }                  
                });
                timer.start(); // Inicia la animación
            } else {
                JOptionPane.showMessageDialog(null, "Monedas insuficientes para jugar", "Estado insuficiente de monedas", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingresar apuesta para jugar", "Ingreso de Monedas", JOptionPane.INFORMATION_MESSAGE);
        }
        jBtnApuesta.setEnabled(true);
    }//GEN-LAST:event_jBtnApuestaActionPerformed

    private void jBtnIngresoCreditosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnIngresoCreditosActionPerformed
        if (!(jTxtIngresoCredito.getText().isEmpty())) {
            datosMaquina.setHandel(true);
            // Se agregan los válores al Label correspondiente
            monedasDisponibles = conversionMonedas(jTxtIngresoCredito.getText());
            jLabelCreditos.setText(String.valueOf(monedasDisponibles));
            jTxtIngresoCredito.setText(null);
            return;
        }
        JOptionPane.showMessageDialog(null, "Ingrese sus monedas por favor, para poder jugar", "Ingreso de Monedas", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jBtnIngresoCreditosActionPerformed

